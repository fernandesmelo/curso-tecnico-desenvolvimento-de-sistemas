# 5. Escreva um programa que solicite ao usuário a temperatura em graus
# Celsius e exiba a temperatura em graus Fahrenheit. (Dica: a fórmula
# para converter Celsius em Fahrenheit é F = C * 1,8 + 32)

temperatura_celsius = float(input("Digite a temperatura em Celsius: "))

temperatura_fahrenheit = temperatura_celsius * 1.8 + 32

print("A temperatura em Fahrenheit é:", temperatura_fahrenheit, "°F")

