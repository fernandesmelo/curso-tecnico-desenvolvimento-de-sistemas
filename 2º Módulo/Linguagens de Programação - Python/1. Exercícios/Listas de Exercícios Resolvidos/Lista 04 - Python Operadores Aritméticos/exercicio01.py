# 1. Escreva um programa que solicite ao usuário dois números e exiba a soma,
# a subtração, a multiplicação e a divisão dos dois números.

numero1 = float(input("Digite o primeiro número: "))
numero2 = float(input("Digite o segundo número: "))

print("Soma:", numero1 + numero2)
print("Subtração:", numero1 - numero2)
print("Multiplicação:", numero1 * numero2)
print("Divisão:", numero1 / numero2)

