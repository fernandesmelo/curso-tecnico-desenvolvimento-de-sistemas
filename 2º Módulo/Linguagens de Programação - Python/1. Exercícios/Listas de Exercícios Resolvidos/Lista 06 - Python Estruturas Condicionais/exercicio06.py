# 6. Escreva um programa que receba a idade de uma pessoa e verifique se ela é maior ou
# igual a 18 anos. Se sim, imprima "Você é maior de idade". Caso contrário, imprima "Você
# é menor de idade". Use a instrução IF/ELSE.

idade = int(input("Digite a sua idade: "))  

if idade >= 18:  
    print("Você é maior de idade.")  
else:
    print("Você é menor de idade.") 
