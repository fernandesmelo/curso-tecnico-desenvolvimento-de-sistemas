# 8. Crie um programa que receba a nota de um aluno e verifique se ele foi aprovado ou
# reprovado. Considere que a nota mínima para aprovação é 7. Use a instrução IF/ELSE.

nota = float(input("Digite a nota do aluno: "))

if nota >= 7:
    print("Aluno aprovado!")
else:
    print("Aluno reprovado!")
