# Desafio de Inversão de Texto:

# Utilizando a linguagem de programação Python, crie um algoritmo que receba uma frase como entrada e retorne 
# uma nova frase em que as palavras estejam invertidas.

texto = input("Escreva uma palavra ou frase: ")  # Solicita ao usuário que digite uma palavra ou frase e armazena na variável "texto".

print(texto[::-1])  # Imprime o texto invertido.



